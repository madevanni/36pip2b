                        <p style="text-indent: 0.25in">
                            Sejak lahirnya Undang-Undang No. 28 tahun 2002 tentang Bangunan Gedung (UUBG), dibutuhkan suatu lembaga yang diharapkan dapat memberikan kemudahan layanan dan akses untuk mendapatkan informasi kepada berbagai unsur: Perencana, Pelaksana, Pengusaha Bahan Bangunan, Pihak Pemerintah, Masyarakat serta kalangan akademis. Lembaga Pengembangan Permukiman dan Bangunan (PIP2B) merupakan suatu lembaga yang mampu mewujudkan hal tersebut.
